require("dotenv").config();
let mongoose = require('mongoose');
mongoose.connect(process.env.PTB_DATABASE_URL, {
    useNewUrlParser: true,
    useUnifiedTopology: true,
    dbName: process.env.PTB_DATABASE
});
let db = mongoose.connection;

// ===== ===== Article(s) ===== =====
db.once('open', function() {
    let schema = new mongoose.Schema(
        {
            date: String,
            title: String,
            content: String,
            image: String
        },
        {
            typeKey: "$type"
        }
    );
    
    let Article = mongoose.model("article", schema, "articles");
    
    articlesData.forEach(articleData => {
        let article = new Article({
            date: articleData.date,
            title: articleData.title,
            content: articleData.content,
            image: articleData.image
        });

        article.save(function(err, article) {
            if (err) console.log(err);
            console.log("Article " + article.title + " successfully stored on database!");
        });
    });
});