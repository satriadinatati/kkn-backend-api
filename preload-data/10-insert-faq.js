require("dotenv").config();
let mongoose = require('mongoose');
mongoose.connect(process.env.PTB_DATABASE_URL, {
    useNewUrlParser: true,
    useUnifiedTopology: true,
    dbName: process.env.PTB_DATABASE
});
let db = mongoose.connection;

// ===== ===== Faq(s) ===== =====
db.once('open', function() {
    let schema = new mongoose.Schema(
        {
            number: Number,
            question: String,
            answer: String
        },
        {
            typeKey: "$type"
        }
    );
    
    let Faq = mongoose.model("faq", schema, "faqs");
    
    faqsData.forEach(faqData => {
        let faq = new Faq({
            number: faqData.number,
            question: faqData.question,
            answer: faqData.answer
        });

        faq.save(function(err, faq) {
            if (err) console.log(err);
            console.log("Question number " + faq.number + " successfully stored on database!");
        });
    });
});